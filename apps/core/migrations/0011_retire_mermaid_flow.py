"""Retire the Mermaid flow table with reversible backup support."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from django.conf import settings
from django.db import migrations

BACKUP_RELATIVE_PATH = Path("var/migration_backups/mermaid_flow.json")
FLOW_TABLE_NAME = "mermaid_flow"
FLOW_COLUMNS = (
    "id",
    "is_seed_data",
    "is_user_data",
    "is_deleted",
    "name",
    "description",
    "definition",
)


def _backup_path() -> Path:
    """Return the filesystem path used to persist Mermaid flow export data."""

    return Path(settings.BASE_DIR) / BACKUP_RELATIVE_PATH


def _flow_table_exists(schema_editor: Any) -> bool:
    """Return whether the Mermaid flow table currently exists in the active database."""

    with schema_editor.connection.cursor() as cursor:
        return FLOW_TABLE_NAME in schema_editor.connection.introspection.table_names(cursor)


def _create_flow_table_sql(schema_editor: Any) -> str:
    """Build vendor-specific SQL to recreate the retired Mermaid flow table."""

    vendor = schema_editor.connection.vendor
    quoted_table = schema_editor.quote_name(FLOW_TABLE_NAME)

    if vendor == "postgresql":
        id_column = "BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY"
    elif vendor == "sqlite":
        id_column = "INTEGER PRIMARY KEY AUTOINCREMENT"
    elif vendor == "mysql":
        id_column = "BIGINT AUTO_INCREMENT PRIMARY KEY"
    else:
        id_column = "BIGINT PRIMARY KEY"

    return (
        f"CREATE TABLE IF NOT EXISTS {quoted_table} ("
        f"id {id_column}, "
        "is_seed_data BOOLEAN NOT NULL DEFAULT FALSE, "
        "is_user_data BOOLEAN NOT NULL DEFAULT FALSE, "
        "is_deleted BOOLEAN NOT NULL DEFAULT FALSE, "
        "name VARCHAR(255) NOT NULL UNIQUE, "
        "description TEXT NOT NULL DEFAULT '', "
        "definition TEXT NOT NULL"
        ")"
    )


def export_and_drop_mermaid_flow(apps: Any, schema_editor: Any) -> None:
    """Export Mermaid flow rows to JSON and then drop the retired table."""

    del apps
    if not _flow_table_exists(schema_editor):
        return

    quoted_table = schema_editor.quote_name(FLOW_TABLE_NAME)
    with schema_editor.connection.cursor() as cursor:
        cursor.execute(
            "SELECT id, is_seed_data, is_user_data, is_deleted, name, description, definition "
            f"FROM {quoted_table} ORDER BY id"
        )
        rows = [dict(zip(FLOW_COLUMNS, row, strict=True)) for row in cursor.fetchall()]

    backup_path = _backup_path()
    backup_path.parent.mkdir(parents=True, exist_ok=True)
    backup_path.write_text(json.dumps(rows, indent=2), encoding="utf-8")

    schema_editor.execute(f"DROP TABLE IF EXISTS {quoted_table}")


def restore_mermaid_flow(apps: Any, schema_editor: Any) -> None:
    """Recreate the Mermaid flow table and restore exported rows during rollback."""

    del apps
    schema_editor.execute(_create_flow_table_sql(schema_editor))

    backup_path = _backup_path()
    if not backup_path.exists():
        return

    rows = json.loads(backup_path.read_text(encoding="utf-8"))
    if not rows:
        return

    quoted_table = schema_editor.quote_name(FLOW_TABLE_NAME)
    quoted_columns = ", ".join(schema_editor.quote_name(column) for column in FLOW_COLUMNS)
    placeholders = ", ".join(["%s"] * len(FLOW_COLUMNS))

    with schema_editor.connection.cursor() as cursor:
        cursor.executemany(
            f"INSERT INTO {quoted_table} ({quoted_columns}) VALUES ({placeholders})",
            [tuple(row[column] for column in FLOW_COLUMNS) for row in rows],
        )


class Migration(migrations.Migration):
    """Remove Mermaid flow persistence while retaining reversible data recovery."""

    dependencies = [
        ("core", "0010_mcpapikey"),
    ]

    operations = [
        migrations.RunPython(export_and_drop_mermaid_flow, restore_mermaid_flow),
    ]
