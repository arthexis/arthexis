"""Generate locked requirements files from ``pyproject.toml`` dependency declarations."""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

try:
    import tomllib
except ModuleNotFoundError as exc:  # pragma: no cover
    raise RuntimeError("Python 3.11+ is required to parse pyproject.toml") from exc


PYPROJECT_PATH = Path("pyproject.toml")

RUNTIME_GROUP = "__runtime__"
PROFILE_DEFINITIONS: tuple[tuple[str, Path, tuple[str, ...], str], ...] = (
    (
        RUNTIME_GROUP,
        Path("requirements-runtime.txt"),
        (),
        "# This file is generated from pyproject.toml project dependencies.",
    ),
    (
        "preview",
        Path("requirements-preview.txt"),
        ("preview",),
        "# This file is generated from pyproject.toml runtime and preview dependencies.",
    ),
    (
        "test",
        Path("requirements-test.txt"),
        ("test",),
        "# This file is generated from pyproject.toml runtime and test dependencies.",
    ),
    (
        "docs",
        Path("requirements-docs.txt"),
        ("docs",),
        "# This file is generated from pyproject.toml runtime and docs dependencies.",
    ),
    (
        "hardware",
        Path("requirements-hw.txt"),
        ("hardware",),
        "# This file is generated from pyproject.toml runtime and hardware dependencies.",
    ),
    (
        "video",
        Path("requirements-video.txt"),
        ("video",),
        "# This file is generated from pyproject.toml runtime and video dependencies.",
    ),
    (
        "nodes",
        Path("requirements-nodes.txt"),
        ("nodes",),
        "# This file is generated from pyproject.toml runtime and nodes dependencies.",
    ),
    (
        "ci",
        Path("requirements-ci.txt"),
        ("preview", "test", "docs", "hardware", "video", "nodes", "ci"),
        "# This file is generated from pyproject.toml runtime and CI-only dependency profiles.",
    ),
)

LEGACY_RUNTIME_REQUIREMENTS_PATH = Path("requirements.txt")
HEADER_COMMON_LINES = (
    "# Regenerate with: python scripts/generate_requirements.py",
    "# Do not edit this file manually.",
)


class RequirementsGenerationError(RuntimeError):
    """Raised when requirement generation cannot proceed."""


def _load_dependency_groups(pyproject_path: Path) -> tuple[list[str], dict[str, list[str]]]:
    """Return runtime and optional dependency groups from ``pyproject.toml``.

    Parameters
    ----------
    pyproject_path:
        Path to the canonical dependency declaration file.

    Returns
    -------
    tuple[list[str], dict[str, list[str]]]
        Runtime dependency specifiers and optional dependency groups.

    Raises
    ------
    FileNotFoundError
        If ``pyproject.toml`` does not exist.
    RequirementsGenerationError
        If the project dependency sections are missing or malformed.
    """

    if not pyproject_path.exists():
        raise FileNotFoundError(f"Missing dependency source file: {pyproject_path}")

    data = tomllib.loads(pyproject_path.read_text(encoding="utf-8"))
    project_data = data.get("project")
    if not isinstance(project_data, dict):
        raise RequirementsGenerationError("Missing [project] section in pyproject.toml")

    runtime_dependencies = project_data.get("dependencies")
    if not isinstance(runtime_dependencies, list):
        raise RequirementsGenerationError(
            "Expected [project].dependencies to be a list of requirement specifiers"
        )

    optional_dependencies = project_data.get("optional-dependencies", {})
    if not isinstance(optional_dependencies, dict):
        raise RequirementsGenerationError(
            "Expected [project.optional-dependencies] to be a table of dependency lists"
        )

    invalid_runtime_entries = [entry for entry in runtime_dependencies if not isinstance(entry, str)]
    if invalid_runtime_entries:
        raise RequirementsGenerationError("Found non-string runtime dependency entries")

    normalized_optional_dependencies: dict[str, list[str]] = {}
    for group_name, group_dependencies in optional_dependencies.items():
        if not isinstance(group_dependencies, list):
            raise RequirementsGenerationError(
                f"Expected [project.optional-dependencies].{group_name} to be a list"
            )
        invalid_group_entries = [entry for entry in group_dependencies if not isinstance(entry, str)]
        if invalid_group_entries:
            raise RequirementsGenerationError(
                f"Found non-string dependency entries in optional group: {group_name}"
            )
        normalized_optional_dependencies[group_name] = group_dependencies

    return runtime_dependencies, normalized_optional_dependencies


def _build_requirements_text(header_line: str, dependencies: list[str]) -> str:
    """Build normalized requirements content with generation metadata.

    Parameters
    ----------
    header_line:
        The profile-specific metadata line to place at the top of the file.
    dependencies:
        Requirement specifiers to render into the requirements file.

    Returns
    -------
    str
        The normalized file content, terminated with a newline.
    """

    lines = [header_line, *HEADER_COMMON_LINES, "", *sorted(set(dependencies), key=str.lower)]
    return "\n".join(lines) + "\n"


def _resolve_profile_dependencies(
    runtime_dependencies: list[str],
    optional_dependencies: dict[str, list[str]],
) -> dict[Path, str]:
    """Resolve requirements file content for every install profile.

    Parameters
    ----------
    runtime_dependencies:
        Dependencies required for all runtime installs.
    optional_dependencies:
        Optional dependency groups keyed by extra name.

    Returns
    -------
    dict[Path, str]
        Mapping of output paths to generated requirements content.

    Raises
    ------
    RequirementsGenerationError
        If a required optional dependency group is missing.
    """

    profile_content: dict[Path, str] = {}
    runtime_text = ""

    for profile_name, output_path, extra_groups, header_line in PROFILE_DEFINITIONS:
        profile_dependencies = list(runtime_dependencies)
        for group_name in extra_groups:
            if group_name not in optional_dependencies:
                raise RequirementsGenerationError(
                    f"Missing optional dependency group '{group_name}' needed for {profile_name}"
                )
            profile_dependencies.extend(optional_dependencies[group_name])

        profile_text = _build_requirements_text(header_line, profile_dependencies)
        profile_content[output_path] = profile_text
        if profile_name == RUNTIME_GROUP:
            runtime_text = profile_text

    profile_content[LEGACY_RUNTIME_REQUIREMENTS_PATH] = runtime_text
    return profile_content


def generate_requirements(pyproject_path: Path) -> None:
    """Write all generated requirements files from ``pyproject.toml``.

    Parameters
    ----------
    pyproject_path:
        Path to the canonical dependency declaration file.
    """

    runtime_dependencies, optional_dependencies = _load_dependency_groups(pyproject_path)
    for output_path, content in _resolve_profile_dependencies(
        runtime_dependencies,
        optional_dependencies,
    ).items():
        output_path.write_text(content, encoding="utf-8")


def check_requirements(pyproject_path: Path) -> bool:
    """Return whether committed requirements content matches generated output.

    Parameters
    ----------
    pyproject_path:
        Path to the canonical dependency declaration file.

    Returns
    -------
    bool
        ``True`` when every generated requirements file matches the committed copy.
    """

    runtime_dependencies, optional_dependencies = _load_dependency_groups(pyproject_path)
    expected_outputs = _resolve_profile_dependencies(runtime_dependencies, optional_dependencies)

    for output_path, expected_content in expected_outputs.items():
        current_content = output_path.read_text(encoding="utf-8") if output_path.exists() else ""
        if current_content != expected_content:
            return False
    return True


def main() -> int:
    """Parse CLI arguments and generate or validate requirements output.

    Returns
    -------
    int
        Process exit code.
    """

    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check",
        action="store_true",
        help="Verify requirements files match generated output without writing files.",
    )
    args = parser.parse_args()

    if args.check:
        if check_requirements(PYPROJECT_PATH):
            print("Generated requirements files are up to date")
            return 0
        print(
            "Generated requirements files are out of date. "
            "Run: python scripts/generate_requirements.py",
            file=sys.stderr,
        )
        return 1

    generate_requirements(PYPROJECT_PATH)
    print("Regenerated requirements profile files from pyproject.toml")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
